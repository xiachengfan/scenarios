def set_intersection(set1: set, set2: set) -> set:
    """
    Returns a new set containing the elements that are present in both set1 and set2.

    Args:
    set1 (set): A set containing unique elements.
    set2 (set): A set containing unique elements.

    Returns:
    set: A new set containing the elements that are present in both set1 and set2.
    """

    return set1.intersection(set2)
