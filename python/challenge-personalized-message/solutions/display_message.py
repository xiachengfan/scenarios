# This function takes in two parameters, 'name' and 'age'.
# The parameters are used in a formatted string to display a message.
def display_message(name, age):
    # The formatted string contains the 'name' and 'age' variables passed as arguments.
    # The 'f' in front of the string indicates it's an f-string, which allows variables to be directly embedded in the string.
    print(f"Hello, my name is {name} and I am {age} years old.")
